import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const tool_dir = path.dirname(fileURLToPath(import.meta.url));
const repo_root = path.resolve(tool_dir, '..');
const output_file = 'click-play-boom.html';

const css_files = [
  'src/styles/tokens.css',
  'src/styles/layout.css',
  'src/styles/navigator.css',
  'src/styles/editor.css',
  'src/styles/results.css',
  'src/styles/vendors/uplot.css',
];

const script_files = [
  'src/scripts/main.js',
  'src/scripts/storage.js',
  'src/scripts/sidebar/snippets.js',
  'src/scripts/sidebar/navigator-state.js',
  'src/scripts/query-editor/editor.js',
  'src/scripts/query-editor/autocomplete.js',
  'src/scripts/sidebar/navigator.js',
  'src/scripts/query-results/query-results.js',
  'src/scripts/sidebar/connections.js',
  'src/scripts/query-results/rendering.js',
  'src/scripts/query-results/charts.js',
  'src/scripts/query-editor/lexer.js',
  'src/scripts/boot.js',
  'src/scripts/query-results/download.js',
];

function readText(relative_path) {
  return fs.readFileSync(path.join(repo_root, relative_path), 'utf8');
}

function withoutFinalNewline(text) {
  return text.replace(/\n+$/, '');
}

function replaceOnce(source, marker, replacement) {
  if (!source.includes(marker)) {
    throw new Error(`Missing build marker: ${marker}`);
  }

  return source.replace(marker, () => replacement);
}

function buildSingleFile() {
  const favicon_svg = readText('src/assets/favicon.svg');
  const favicon_data_url = `data:image/svg+xml;base64,${Buffer.from(favicon_svg, 'utf8').toString('base64')}`;
  const logo_svg = withoutFinalNewline(readText('src/assets/clickhouse-logo.svg'));
  const result_panel_template = withoutFinalNewline(readText('src/templates/result-panel.html'));
  const lexer_wasm_base64 = withoutFinalNewline(readText('src/assets/clickhouse-lexer.wasm.base64'));

  const styles = css_files.map(file => withoutFinalNewline(readText(file))).join('\n\n');
  const scripts = script_files
    .map(file => withoutFinalNewline(readText(file)))
    .join('\n\n')
    .replace('__CLICKHOUSE_LEXER_WASM_BASE64__', lexer_wasm_base64);

  let html = readText('src/index.html');
  html = replaceOnce(html, '<!-- build:generated-notice -->', [
    '<!--',
    '  Generated from src/. Do not edit this file directly.',
    '  Run `npm run build` after changing source files.',
    '-->',
  ].join('\n'));
  html = replaceOnce(html, '__FAVICON_DATA_URL__', favicon_data_url);
  html = replaceOnce(html, '    <!-- build:styles -->', `    <style>\n${styles}\n    </style>`);
  html = replaceOnce(html, '            <!-- build:clickhouse-logo -->', logo_svg);
  html = replaceOnce(html, '        <!-- build:result-panel-template -->', result_panel_template);
  html = replaceOnce(html, '<!-- build:scripts -->', `<script type="text/javascript">\n${scripts}\n</script>`);

  return html.endsWith('\n') ? html : `${html}\n`;
}

function writeAtomically(relative_path, content) {
  const target = path.join(repo_root, relative_path);
  const temp = `${target}.tmp-${process.pid}`;
  fs.writeFileSync(temp, content);
  fs.renameSync(temp, target);
}

function printUsageAndExit() {
  console.error('Usage: node tools/build-single-file.mjs --write|--check');
  process.exit(2);
}

const mode = process.argv[2];
if (mode !== '--write' && mode !== '--check') {
  printUsageAndExit();
}

const generated = buildSingleFile();

if (mode === '--write') {
  writeAtomically(output_file, generated);
  console.log(`Wrote ${output_file}`);
} else {
  const current = readText(output_file);
  if (current !== generated) {
    console.error(`${output_file} is stale. Run npm run build.`);
    process.exit(1);
  }

  console.log(`${output_file} is up to date.`);
}
